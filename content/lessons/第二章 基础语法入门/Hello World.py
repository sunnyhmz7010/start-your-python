# @step.title: print函数
# @step.content:
# `print()` 用来把内容输出到屏幕。
# 
# 当你执行这行代码时，Python 会读取括号里的文本，再把它显示在终端或控制台。
# 
# ![Hello world flow](/course-images/hello-world-flow.svg)
print("Hello, World!")

# @step.id: s2
# @step.type: text
# @step.title: 字符串
# @step.content:
# 双引号里的内容叫**字符串**，也就是文本数据。
# 
# 例如：
# - `"Hello, World!"`
# - `"Sunny"`
# - `"Python 很有趣"`
# 
# 先把它理解成“程序里保存文字的一种方式”。

# @step.id: s3
# @step.type: code
# @step.title: 再试一个输出
# @step.content:
# 你可以把任意文本传给 `print()`。
# 
# 刚开始学的时候，建议多改几个单词再运行，建立“我写什么，程序就输出什么”的直觉。
print("Python 很有趣")
