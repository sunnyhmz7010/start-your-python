# @step.title: 比较结果
# @step.content:
# 比较运算的结果不是数字，而是布尔值：`True` 或 `False`。
# 
# 也就是说，比较本质上是在回答一个问题：这个条件成立吗？
# 
# ![Comparison operators](/course-images/comparison-scale.svg)

# @step.id: s2
# @step.type: code
# @step.title: 常见符号
# @step.content:
# 常见比较符号有：
# - `==` 等于
# - `!=` 不等于
# - `>`、`<`、`>=`、`<=`
print(3 == 3)
print(5 > 8)
print(10 != 6)

